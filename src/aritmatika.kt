    val result:Int = 10+80
    println(result)

    val result2:Int = 10-80
    println(result2)

    var result3:Int = 10*80
    println(result3)

    var result4:Float= 10/-80f
    println(result4)

    var result5: Double = 100.9%8
    println(result5)

    println("#### penyederhanaan ####")
